-- Создаём отдельный справочник единиц измерения
CREATE TABLE единица_измерения (
    id_единицы SERIAL PRIMARY KEY,
    наименование VARCHAR(20) NOT NULL
);

-- Наполняем справочник
INSERT INTO единица_измерения (наименование) VALUES
    ('м'),
    ('м²'),
    ('шт');

-- Добавляем внешний ключ в категория_материала
ALTER TABLE категория_материала
    ADD COLUMN id_единицы INT REFERENCES единица_измерения(id_единицы);

-- Заполняем новый столбец
UPDATE категория_материала SET id_единицы = 1 WHERE единица_измерения = 'м';
UPDATE категория_материала SET id_единицы = 2 WHERE единица_измерения = 'м²';
UPDATE категория_материала SET id_единицы = 3 WHERE единица_измерения = 'шт';

-- Удаляем старый столбец
ALTER TABLE категория_материала DROP COLUMN единица_измерения;